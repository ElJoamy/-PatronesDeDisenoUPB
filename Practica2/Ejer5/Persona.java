public class Persona {
    int ci;
    String name;

    public Persona(int ci, String name) {
        this.ci = ci;
        this.name = name;
    }

    public int getCi() {
        return ci;
    }

    public void setCi(int ci) {
        this.ci = ci;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
